package com.StringBufferandStringBulider;

public class Example1 {

	public static void main(String[] args)
	{
		//StringBuffer
		System.out.println("STRING BUFFER");
		StringBuffer fName=new StringBuffer("Varshini");
		StringBuffer lName=new StringBuffer("devi");
		System.out.println(fName+" "+lName);
		//StringBuilder
		System.out.println("***************");
		System.out.println("STRING BUILDER");
		StringBuilder name=new StringBuilder("Seethala");
		StringBuilder myName=new StringBuilder("devi");
		System.out.println(name+" "+myName);
		

	}

}
