package com.String;

public class Immutable {

	public static void main(String[] args)
	{
		//  String class object are immutable
		String fName="SEETHALA"; 
		System.out.println(fName);
		
		System.out.println("**********");
		String name="SEETHALA";
		name.concat("DEVI"); 
		System.out.println(name);

	}

}
